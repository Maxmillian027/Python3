# Conditions
# Conditions Enables Program Decision Making.
# Given a Condition and if the Conditions is Satisfied
# a Block of Code will Execute else it will not.

a = 2; b = 10

# If Statements
if a == b:
    print('a is equal to b.\n')

if a != b:
    print('a is not equal to b.\n')

if a > b:
    print('a is greater than b.\n')

if a < b:
    print('a is less than b.\n')


input("Press Enter to Continue!\n")


# If-Else Statement
if a == b: print('a is equal to b.\n')

else: print('a is not equal to b.\n')

if a < b: print('a is less than b.\n')

else: print('a is greater than b.\n')


input("Press Enter to Continue!\n")


# ElIf Statement
if a == b:
    print('a is equal to b.')

elif a > b:
    print('a is greater than b.')

else:
    print('a is less than b.')
